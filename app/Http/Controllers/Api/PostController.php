    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($slug)
    {
        $post = Post::where('slug', $slug)->with('category','tags')->first();

        return response()->json([
            'success' => false,
            'results' => $post
        ]);
    }

  
